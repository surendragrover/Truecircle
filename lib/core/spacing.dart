/// Centralized spacing constants to keep UI gaps consistent across the app.
class AppGaps {
  const AppGaps._();

  // Extra small gaps (tight elements)
  static const double xs = 4;
  // Small gaps (chips, inline elements)
  static const double small = 8;
  // Standard section spacing between blocks
  static const double section = 16;
  // Large gaps for major breaks (if needed later)
  static const double large = 24;
}
